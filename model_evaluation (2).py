# Outlier Capping Function (IQR Method)
def outlier_capping(df_feat, column):
    Q1 = df_feat[column].quantile(0.25)
    Q3 = df_feat[column].quantile(0.75)
    IQR = Q3 - Q1
    lower_extreme = Q1 - 1.5 * IQR
    upper_extreme = Q3 + 1.5 * IQR

    df_feat[column] = df_feat[column].apply(
        lambda x: lower_extreme if x < lower_extreme
        else upper_extreme if x > upper_extreme
        else x
    )
# Apply to all numeric columns
