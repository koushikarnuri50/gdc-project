"""model - 5 :Gaussian Mixture Model"""

from sklearn.mixture import GaussianMixture

#Creating GMM Model
gmm = GaussianMixture(n_components=3,covariance_type='full',  n_init=10,random_state=42)

# train and cluster predicting
best_score = -1  # high score
best_k = None # number of clusters
best_model = None # best model
for k in range(2,8):
    gmm = GaussianMixture(
        n_components=k,
        covariance_type='full',
        random_state=42
    )
    labels = gmm.fit_predict(df_pca)
    score = silhouette_score(df_pca, labels)
    print("Clusters:", k, "Score:", score)
    if score > best_score:
        best_score = score
        best_k = k
        best_model = labels
print("\nBest Clusters:", best_k)

df_feat['GMM_Cluster'] = best_model
